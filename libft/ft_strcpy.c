char	*ft_strcpy(char *dst, const char *src)
{
	int		i;

	i = 0;
	while (*src)
	{
		*dst = *src;
		dst++;
		src++;
		i++;
	}
	*dst = '\0';
	dst = dst - i;
	return (dst);
}
