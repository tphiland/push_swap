#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	s;

	i = 0;
	s = 0;
	while (*s1)
	{
		s1++;
		i++;
	}
	while (*s2 && s < n)
	{
		*s1 = *s2;
		s1++;
		s2++;
		s++;
		i++;
	}
	*s1 = '\0';
	s1 = s1 - i;
	return (s1);
}
